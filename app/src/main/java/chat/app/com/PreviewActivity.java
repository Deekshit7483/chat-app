	private void initializeLogic() {
		IMAGE_PATH = FileUtil.getExternalStorageDir().concat("/chatapp/images/");
		VIDEO_PATH = FileUtil.getExternalStorageDir().concat("/chatapp/videos/");
		if (!FileUtil.isExistFile(IMAGE_PATH)) {
			FileUtil.makeDir(IMAGE_PATH);
		}
		if (!FileUtil.isExistFile(VIDEO_PATH)) {
			FileUtil.makeDir(VIDEO_PATH);
		}
		if (getIntent().getStringExtra("mode").equals("image")) {
			Glide.with(getApplicationContext()).load(Uri.parse(getIntent().getStringExtra("url"))).into(imageview1);
			videoview1.setVisibility(View.GONE);
			imageMode = true;
		}
		else {
			if (getIntent().getStringExtra("mode").equals("video")) {
				imageMode = false;
				imageview1.setVisibility(View.GONE);
				videoview1.setVideoURI(Uri.parse(getIntent().getStringExtra("url")));
				SketchwareUtil.showMessage(getApplicationContext(), "Loading... ");
			}
		}
		_initSlideActivity();
	}
	
